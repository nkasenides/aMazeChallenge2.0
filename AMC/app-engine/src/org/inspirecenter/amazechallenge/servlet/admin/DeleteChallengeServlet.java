package org.inspirecenter.amazechallenge.servlet.admin;

import org.inspirecenter.amazechallenge.model.AdminKey;
import org.inspirecenter.amazechallenge.model.Challenge;
import org.inspirecenter.amazechallenge.persistence.DBManager;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

public class DeleteChallengeServlet extends HttpServlet {
    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {

        final String adminKeyInput = req.getParameter("adminKey");
        final String challengeID = req.getParameter("challengeID");

        if (adminKeyInput == null || adminKeyInput.isEmpty()) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=No admin key");
            return;
        }

        if (challengeID == null || challengeID.isEmpty()) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=No challenge ID");
            return;
        }

        //Authenticate:
        final AdminKey adminKey = DBManager.adminKey.get();
        if (adminKey == null) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=No admin key in DB");
            return;
        }

        if (!adminKey.getId().equals(adminKeyInput)) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=Invalid admin key");
            return;
        }

        //Find and remove challenge:
        final Challenge challenge = DBManager.challenge.get(challengeID);
        if (challenge == null) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=Invalid challenge ID. The challenge with this ID does not exist.");
            return;
        }

        final boolean delete = DBManager.challenge.delete(challenge);

        if (delete) {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=Challenge deleted.");
        }
        else {
            resp.sendRedirect("../../admin/viewChallenges.jsp?message=Could not delete challenge.");
        }


    }
}